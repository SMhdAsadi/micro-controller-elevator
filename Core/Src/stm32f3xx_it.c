/* USER CODE BEGIN 0 */
void numberToBcd(int i) {
  int x1 = i&1;
  int x2 = i&2;
  int x3 = i&4;
  int x4 = i&8;
  if(x1 > 0) x1 = 1;
  if(x2 > 0) x2 = 1;
  if(x3 > 0) x3 = 1;
  if(x4 > 0) x4 = 1;
  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_3, x1);
  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_4, x2);
  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_5, x3);
  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_6, x4);
}
