/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */

  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */
  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_1 | GPIO_PIN_2, 0);
  if (seven_segment_digit_controller == 0) {
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_1, 1);
    numberToBcd(user_input);
    seven_segment_digit_controller = 1;
  } else {
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_2, 1);
    numberToBcd(current_floor);
    seven_segment_digit_controller = 0;
  }
  /* USER CODE END TIM2_IRQn 1 */
}

/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */

  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */
  if (is_admin_mode || is_idle) return;

  timer_counter++;

  if (timer_counter % 5 == 0) {
//    buzzer_kon();
  }
  if (timer_counter % (moving_delay / 50) == 0) {
    int target_floor = peek_from_floor_queue();
    if (current_floor < target_floor) {
      current_floor++;
      if (current_floor == target_floor) {
        dequeue_from_floor_queue();
        if (floor_queue.length == 0) {
          is_idle = true;
        }
      }
    } else if (current_floor > target_floor) {
      current_floor--;
      if (current_floor == target_floor) {
        dequeue_from_floor_queue();
        if (floor_queue.length == 0) {
          is_idle = true;
        }
      }
    }
  }
  /* USER CODE END TIM3_IRQn 1 */
}

